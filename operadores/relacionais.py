print('Operadores relacionais sempre vão ter resultados verdadeiro ou falso')

x = 5
y = 12

#verifica se x é maior que y
print('verifica se x é maior que y')
print(x > y)
print('verifica se x é maior ou igual que y')
print(x >= y)
print('verifica se x é menor que y')
print(x < y)
print('verifica se x é menor ou igual que y')
print(x <= y)
print('verifica se x é igual y')
print(x == y)
print('verifica se x é diferente de y')
print(x != y)


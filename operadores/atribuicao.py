#valor de atribuição inicial
resultado = 2
print(resultado)

#alterando valor de atribuição
print('alterando valor de atribuição')
resultado = 'texto'
print(resultado)

#atribuição aditiva
print('atribuição aditiva += 2')
resultado = 5
print(resultado)
resultado += 2  # resultado + 2
print(resultado)

#atribuição subtrativa
print('atribuição Subtrativa -= 1')
resultado = 5
print(resultado)
resultado -= 1  # resultado - 1
print(resultado)

#atribuição multiplicativa
print('atribuição Multiplicativa *= 4')
resultado = 4
print(resultado)
resultado *= 4  # resultado * 4
print(resultado)

#atribuição divisiva
print('atribuição Divisiva /= 2')
resultado = 10
print(resultado)
resultado /= 2  # resultado / 2
print(resultado)

#atribuição modular (resto de divisao)
print('atribuição Modular %= 6')
resultado = 8
print(resultado)
resultado %= 6  # resultado % 6
print(resultado)
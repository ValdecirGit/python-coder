x = 10
y = 3

# operador que opera em 2 operando, ex: x e y
# +3    operador prefix,    operador está antes do operando
# x++   operador postfix    operador etá depois do operando
# x+y   operador infix      operador esta no meio dos operandos
print('operador que opera em 2 operando, ex: x e y')
print(f'adição = {x + y}') 
print(f'subtração = {x - y}') 
print(f'multiplicação = {x * y}') 
print(f'divisão = {x / y}') 
print(f'mod (resto divisão) = {x % y}') 

